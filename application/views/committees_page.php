	FORMDEV Committees
</div>
<div class="body_post" style="text-align:justify;">
	<div>&nbsp;</div>
	<div style="color:#009900; font-weight:bold;">
		Facilitator Outreach Committee
	</div>
	<div>
		As part of FORMDEV's efforts to embody the Lasallian and Gospel value of Love for the Poor, the Outreach Committee provides each and every facilitator with an opportunity to interact and share God's love with those who have been blessed with less. Members of this committee must look for a community that FORMDEV can help significantly (if needed), prepare the materials needed in order to help the community the most, and provide reports on the progress of these outreach efforts.
	</div>
	<div>&nbsp;</div>
	<div style="color:#009900; font-weight:bold;">
		Communications Committee
	</div>
	<div>
		As FORMDEV's primary means of disseminating information to both FORMDEV facilitators, alumni, and the public in general, the Communications Committee is responsible for maintaining FORMDEV's bulletin board, website, and any other social media account associated with FORMDEV. Facilitators in this committee are expected to update the bulletin board's contents (which include the Verse of the Week, Faci of the Week, Bible Study Schedules, and other announcements) every week and to maintain and add more features and content to the official website of FORMDEV.
	</div>
	<div>&nbsp;</div>
	<div style="color:#009900; font-weight:bold;">
		Instructional Materials Committee
	</div>
	<div>
		As the natural result of FORMDEV's goal to acquaint Lasallian students with the life and values of St. John Baptist De La Salle and ultimately the life and values of our Lord Jesus Christ, the Instructional Materials Committee is responsible for keeping the FORMDEV workbook both reliable and interesting to the Lasallians who will be taking up the FORMDEV course. Facilitators of this committee are also responsible for ensuring that the materials for each class activity (such as boxes for the Treasure Bag activity, scrunchies for the Blind Man's Quest activity, and candles and tissue for the Dark Night activity) are adequate for each class that will be holding the activity as well as for writing and updating the guidelines for these activities.
	</div>
	<div>&nbsp;</div>
	<div style="color:#009900; font-weight:bold;">
		Monitoring Committee
	</div>
	<div>
		As a way to keep track of FORMDEV faciliatators' zeal for service, the Monitoring Committee keeps track of facilitator attendance in all FORMDEV activities (which include but are not limited to committee meetings, class meetings, Bible Study sessions, faci outreaches, and general assemblies). Each of the other three committees (Communications, Instructional Materials, and Outreach) are also assigned a monitoring agent (who is part of this committee) who will remind the committee of its goals and deadlines so that the committee will be able to accomplish its goal. At the end of the term, the committee also reviews and evaluates the results of each of the committee plans over the past term and provides a report on the achievements and areas for improvement of each committee.
	</div>
</div>